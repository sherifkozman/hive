def cmd_bump(args: argparse.Namespace) -> int:
    composable_dir = resolve_composable_dir(Path(args.skill_dir))
    if not composable_dir.is_dir():
        print(f"error: composable dir not found: {composable_dir}", file=sys.stderr)
        return 1

    part = args.part
    vpath = version_path(composable_dir)
    old_raw = read_skill_version(composable_dir)
    if old_raw is None:
        # No VERSION file yet: initialize it rather than bumping from 0.0.0.
        old_display = "(none)"
        new = "0.1.0"
    else:
        if not is_semver(old_raw):
            print(f"error: existing VERSION {old_raw!r} is not valid semver (expected X.Y.Z)", file=sys.stderr)
            return 1
        old_display = old_raw
        new = bump_semver(old_raw, part)

    vpath.write_text(f"{new}\n", encoding="utf-8")
    print(f"{skill_name_for(composable_dir)}: {old_display} -> {new}")
    return 0


