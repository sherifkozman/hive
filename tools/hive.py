class Reporter:
    def __init__(self) -> None:
        self.lines: list[str] = []
        self.counts = {"PASS": 0, "WARN": 0, "FAIL": 0}

    def line(self, status: str, msg: str) -> None:
        self.counts[status] += 1
        self.lines.append(f"[{status}] {msg}")

    def has_fail(self) -> bool:
        return self.counts["FAIL"] > 0

    def summary(self) -> str:
        return f"{self.counts['PASS']} pass, {self.counts['WARN']} warn, {self.counts['FAIL']} fail"


def lint_skill(composable_dir: Path, r: Reporter) -> None:
    skill_name = skill_name_for(composable_dir)
    minis = mini_files(composable_dir)
    mini_names = [p.name for p in minis]

    index_path = composable_dir / "INDEX.md"
    index_text = index_path.read_text(encoding="utf-8") if index_path.is_file() else None

    if index_text is None:
        r.line("FAIL", f"{skill_name}: INDEX.md not found at {index_path}")
    else:
        r.line("PASS", f"{skill_name}: INDEX.md exists")

        # Every mini mentioned exactly once; no mini unlisted.
        for name in mini_names:
            count = index_text.count(name)
            if count == 0:
                r.line("FAIL", f"{skill_name}: mini '{name}' is not referenced in INDEX.md")
            elif count > 1:
                r.line("FAIL", f"{skill_name}: mini '{name}' is referenced {count} times in INDEX.md (expected exactly once)")
            else:
                r.line("PASS", f"{skill_name}: mini '{name}' referenced exactly once in INDEX.md")

        # Index word count budget.
        word_count = len(index_text.split())
        threshold = 300 if len(minis) > 12 else 200
        if word_count >= threshold:
            r.line("WARN", f"{skill_name}: INDEX.md is {word_count} words (budget < {threshold} for {len(minis)} minis)")
        else:
            r.line("PASS", f"{skill_name}: INDEX.md word count {word_count} (< {threshold})")

        # Knowledge-free heuristic: no single line should read like real content.
        # The SPEC-mandated loading-policy header (§3.6) is exempt: it is
        # required verbatim and is loading metadata, not knowledge.
        long_lines = [
            ln.strip() for ln in index_text.splitlines()
            if len(ln.split()) > 30 and not ln.strip().startswith("Loading policy:")
        ]
        if long_lines:
            r.line("WARN", f"{skill_name}: INDEX.md has {len(long_lines)} line(s) over ~30 words (looks like knowledge, not a menu)")
        else:
            r.line("PASS", f"{skill_name}: INDEX.md lines all under ~30 words")

        # 00-core always-load marking.
        core_path = composable_dir / "mini" / "00-core.md"
        if core_path.is_file():
            core_lines = [ln for ln in index_text.splitlines() if "00-core.md" in ln]
            marked = any(
                "always load" in ln.lower() or "always-load" in ln.lower()
                for ln in core_lines
            )
            if marked:
                r.line("PASS", f"{skill_name}: 00-core.md is marked always-load in INDEX.md")
            else:
                r.line("FAIL", f"{skill_name}: 00-core.md exists but is not marked always-load in INDEX.md")
        else:
            r.line("PASS", f"{skill_name}: no 00-core.md present (not required)")

    # No mini unlisted: covered above per-mini when INDEX.md exists. Also
    # flag stray files in mini/ that aren't .md (informational only; skip).

    if not minis:
        r.line("WARN", f"{skill_name}: no mini/*.md files found")

    for mini in minis:
        raw = mini.read_text(encoding="utf-8")
        fm = split_frontmatter(raw)

        if not fm.body.strip():
            r.line("FAIL", f"{skill_name}/{mini.name}: file is empty after frontmatter")
        else:
            r.line("PASS", f"{skill_name}/{mini.name}: nonempty")

        if any(H1_RE.match(ln) for ln in fm.body.splitlines()):
            r.line("PASS", f"{skill_name}/{mini.name}: has an H1")
        else:
            r.line("FAIL", f"{skill_name}/{mini.name}: missing an H1 (# Title)")

        if raw.startswith("---"):
            if fm.data is None or fm.errors:
                for err in (fm.errors or ["frontmatter present but unparsable"]):
                    r.line("FAIL", f"{skill_name}/{mini.name}: invalid frontmatter: {err}")
            else:
                unknown = sorted(set(fm.data) - KNOWN_FRONTMATTER_KEYS)
                if unknown:
                    r.line("FAIL", f"{skill_name}/{mini.name}: unknown frontmatter key(s): {unknown}")
                else:
                    r.line("PASS", f"{skill_name}/{mini.name}: frontmatter keys OK ({sorted(fm.data)})")

                for key in LIST_KEYS & set(fm.data):
                    values = fm.data[key]
                    if isinstance(values, str):
                        values = [values]
                    bad = [v for v in values if not PATH_LIKE_RE.match(v)]
                    if bad:
                        r.line("FAIL", f"{skill_name}/{mini.name}: {key} has non-path-like value(s): {bad}")
                    else:
                        r.line("PASS", f"{skill_name}/{mini.name}: {key} values look like paths")

                # version: optional; silent when absent, FAIL on malformed semver.
                if "version" in fm.data:
                    value = fm.data["version"]
                    if isinstance(value, str) and is_semver(value):
                        r.line("PASS", f"{skill_name}/{mini.name}: version {value!r} is valid semver")
                    else:
                        r.line("FAIL", f"{skill_name}/{mini.name}: version {value!r} is not valid semver (expected X.Y.Z)")
        else:
            r.line("PASS", f"{skill_name}/{mini.name}: no frontmatter (ok, optional)")

    # Skill-level VERSION file: optional; silent when absent, FAIL on malformed semver.
    skill_version = read_skill_version(composable_dir)
    if skill_version is not None:
        if is_semver(skill_version):
            r.line("PASS", f"{skill_name}: VERSION is valid semver ({skill_version})")
        else:
            r.line("FAIL", f"{skill_name}: VERSION {skill_version!r} is not valid semver (expected X.Y.Z)")

    # BUNDLE.md staleness.
    bundle_path = composable_dir / "BUNDLE.md"
    if not minis:
        pass
    elif not bundle_path.is_file():
        r.line("WARN", f"{skill_name}: BUNDLE.md missing; run `compile`")
    else:
        expected = build_bundle_text(skill_name, minis, skill_version=skill_version)
        actual = bundle_path.read_text(encoding="utf-8")
        if actual != expected:
            r.line("FAIL", f"{skill_name}: BUNDLE.md is stale (does not match mini/*.md); re-run `compile`")
        else:
            r.line("PASS", f"{skill_name}: BUNDLE.md is up to date")

    # presets/*.md staleness (convention). Mirrors the BUNDLE.md check above:
    # each preset is recomputed from mini/ + presets.json (the same inputs
    # `compile` uses) and compared byte-for-byte. A presets/ directory with no
    # presets.json cannot be recomputed, so its staleness is unverifiable and is
    # flagged so the definitions get captured (see docs/SPEC.md §7).
    presets_dir = composable_dir / "presets"
    presets_json_path = composable_dir / "presets.json"
    preset_specs = load_presets_json(composable_dir)
    existing_preset_files = sorted(presets_dir.glob("*.md")) if presets_dir.is_dir() else []

    if not minis:
        # No minis = no source of truth to recompute presets from; if preset
        # artifacts or definitions still exist, that is drift, not a case to
        # skip silently (council review 80c484e3).
        if preset_specs or existing_preset_files:
            r.line("FAIL", f"{skill_name}: presets/ or presets.json present but no mini/*.md to build them from")
    elif preset_specs:
        for name, ids in preset_specs.items():
            resolved, unmatched = resolve_preset_ids(minis, ids)
            preset_path = presets_dir / f"{name}.md"
            if unmatched:
                r.line("FAIL", f"{skill_name}: preset {name!r} references unknown minis: {unmatched}")
            elif not preset_path.is_file():
                # A declared preset whose compiled file is absent is stale in the
                # same sense as a mismatched one: `compile` would produce it.
                r.line("FAIL", f"{skill_name}: presets/{name}.md declared in presets.json but missing; re-run `compile`")
            else:
                expected = build_bundle_text(
                    skill_name, resolved, title_suffix=f" (preset: {name})", skill_version=skill_version,
                )
                if preset_path.read_text(encoding="utf-8") != expected:
                    r.line("FAIL", f"{skill_name}: presets/{name}.md is stale (does not match mini/*.md); re-run `compile`")
                else:
                    r.line("PASS", f"{skill_name}: presets/{name}.md is up to date")
        # Orphan preset files on disk with no presets.json entry are never
        # regenerated by `compile`, so their staleness cannot be verified.
        defined = {f"{name}.md" for name in preset_specs}
        for orphan in [q.name for q in existing_preset_files if q.name not in defined]:
            r.line("WARN", f"{skill_name}: presets/{orphan} has no entry in presets.json; preset staleness cannot be verified")
    elif existing_preset_files:
        if presets_json_path.is_file():
            r.line("WARN", f"{skill_name}: presets/ present but presets.json defines no presets (malformed or empty); preset staleness cannot be verified")
        else:
            r.line("WARN", f"{skill_name}: presets/ present but no presets.json — preset staleness cannot be verified; add presets.json")


def cmd_lint(args: argparse.Namespace) -> int:
    composable_dir = resolve_composable_dir(Path(args.skill_dir))
    if not composable_dir.is_dir():
        print(f"error: composable dir not found: {composable_dir}", file=sys.stderr)
        return 1
    r = Reporter()
    lint_skill(composable_dir, r)
    print("\n".join(r.lines))
    print(f"\n{r.summary()}")
    return 1 if r.has_fail() else 0


