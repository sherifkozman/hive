# parity
# --------------------------------------------------------------------------

def normalize_words(text: str) -> set[str]:
    words = re.findall(r"[A-Za-z][A-Za-z']+", text.lower())
    return {w for w in words if len(w) >= 4 and w not in STOPWORDS}


def heading_is_covered(heading: str, mini_text_lower: str, mini_words: set[str]) -> bool:
    clean = re.sub(r"[^A-Za-z0-9 ]", " ", heading).strip().lower()
    if clean and clean in mini_text_lower:
        return True
    sig = normalize_words(heading)
    if not sig:
        return False
    overlap = sig & mini_words
    return len(overlap) / len(sig) >= 0.6


def cmd_parity(args: argparse.Namespace) -> int:
    composable_dir = resolve_composable_dir(Path(args.skill_dir))
    source_dir = Path(args.source_dir)
    if not composable_dir.is_dir():
        print(f"error: composable dir not found: {composable_dir}", file=sys.stderr)
        return 1
    if not source_dir.is_dir():
        print(f"error: source dir not found: {source_dir}", file=sys.stderr)
        return 1

    skill_name = skill_name_for(composable_dir)
    minis = mini_files(composable_dir)
    mini_bodies = [strip_frontmatter_body(p.read_text(encoding="utf-8")) for p in minis]
    mini_union_text = "\n".join(mini_bodies)
    mini_tokens = toks(mini_union_text)
    mini_words = normalize_words(mini_union_text)
    mini_text_lower = mini_union_text.lower()

    source_files = sorted(source_dir.rglob("*.md"))
    if not source_files:
        print(f"error: no *.md files found under {source_dir}", file=sys.stderr)
        return 1
    source_texts = {p: p.read_text(encoding="utf-8") for p in source_files}
    source_tokens = sum(toks(t) for t in source_texts.values())

    ratio = (mini_tokens / source_tokens) if source_tokens else 0.0

    print(f"Parity report: {skill_name}  (source: {source_dir})")
    print(f"  source files : {len(source_files)}")
    print(f"  source tokens: {fmt_toks(source_tokens)}")
    print(f"  mini tokens  : {fmt_toks(mini_tokens)}  ({len(minis)} minis)")
    print(f"  ratio        : {ratio:.1%}")

    fail = False
    if ratio < 0.85:
        print(f"[FAIL] mini tokens are only {ratio:.1%} of source tokens (< 85%); summarization suspected")
        fail = True
    else:
        print(f"[PASS] mini tokens are {ratio:.1%} of source tokens (>= 85%)")

    dropped = []
    for path, text in source_texts.items():
        for line in text.splitlines():
            m = H2_H3_RE.match(line.strip())
            if not m:
                continue
            heading = m.group(2).strip()
            if not heading:
                continue
            if not heading_is_covered(heading, mini_text_lower, mini_words):
                dropped.append(f"{path.relative_to(source_dir)}: {heading}")

    if dropped:
        print(f"\n[WARN] {len(dropped)} source heading(s) with no fuzzy match in any mini (possibly dropped):")
        for d in dropped:
            print(f"    - {d}")
    else:
        print("\n[PASS] every source ## / ### heading has a fuzzy match in the minis")

    return 1 if fail else 0


