#!/usr/bin/env python3
"""hive.py: CCS (Compiled Composable Skills) framework CLI for Hive.

Tooling for the composable skill layout used under any <skill-root>/composable/
directory. A skills root MAY organize skills into category subdirectories
(e.g. skills/authored/<name>/, skills/converted/<name>/) or lay them out flat
(skills/<name>/); discovery (`report`) recurses to find every composable/ dir
at any depth:

    skills/<name>/composable/
        INDEX.md              # loading menu: which mini to load when
        presets.json          # optional: {"preset-name": ["01", "04", ...]}
        VERSION                # optional: single semver line, skill-level version
        mini/
            00-core.md         # optional, always-loaded foundation
            01-....md
            ...
        BUNDLE.md              # compiled: all minis concatenated, generated
        presets/<name>.md      # compiled: a named subset, generated

Minis may start with a YAML-ish frontmatter block:

    ---
    pairs-with:
      - 09-refactoring-and-communication.md
    version: 1.0.0
    ---

Known frontmatter keys: model-hint, effort-hint, requires, pairs-with, version.

Subcommands: compile, lint, parity, report, bump. See --help on each.

Python 3.11, stdlib only.
"""
from __future__ import annotations

import argparse
import json
import re
import sys
from dataclasses import dataclass, field
from pathlib import Path

KNOWN_FRONTMATTER_KEYS = {"model-hint", "effort-hint", "requires", "pairs-with", "version"}
LIST_KEYS = {"requires", "pairs-with"}
PATH_LIKE_RE = re.compile(r"^[A-Za-z0-9_.\-]+(?:/[A-Za-z0-9_.\-]+)*\.md$")
H1_RE = re.compile(r"^#\s+\S")
H2_H3_RE = re.compile(r"^(#{2,3})\s+(.*)$")
SEMVER_RE = re.compile(r"^\d+\.\d+\.\d+$")
STOPWORDS = {
    "the", "and", "for", "with", "from", "into", "your", "this", "that",
    "when", "what", "how", "are", "was", "were", "have", "has", "not",
    "over", "under", "about", "per", "via", "vs", "step", "steps",
}

GENERATED_MARKER = "<!-- GENERATED by tools/hive.py compile. Do not hand-edit. -->"


# --------------------------------------------------------------------------
# Core parsing helpers
# --------------------------------------------------------------------------

def toks(text: str) -> float:
    """Token estimate: chars / 4."""
    return len(text) / 4.0


def fmt_toks(n: float) -> str:
    return str(round(n))


@dataclass
class FrontmatterResult:
    data: dict | None  # None => no frontmatter block present
    body: str
    errors: list[str] = field(default_factory=list)


def split_frontmatter(text: str) -> FrontmatterResult:
    """Parse an optional leading YAML-ish frontmatter block.

    Only supports the shapes actually used in this repo: top-level
    ``key: value`` scalars and ``key:`` followed by ``  - item`` list
    entries (also tolerates an inline ``[a, b]`` list).
    """
    if not text.startswith("---"):
        return FrontmatterResult(None, text, [])

    lines = text.split("\n")
    if lines[0].strip() != "---":
        return FrontmatterResult(None, text, [])

    end_idx = None
    for i in range(1, len(lines)):
        if lines[i].strip() == "---":
            end_idx = i
            break

    if end_idx is None:
        return FrontmatterResult(None, text, ["unterminated frontmatter block (no closing '---')"])

    fm_lines = lines[1:end_idx]
    body = "\n".join(lines[end_idx + 1:])

    data: dict[str, object] = {}
    errors: list[str] = []
    i = 0
    while i < len(fm_lines):
        line = fm_lines[i]
        if not line.strip():
            i += 1
            continue
        if line[0].isspace():
            errors.append(f"unexpected indented line outside a list: {line!r}")
            i += 1
            continue
        m = re.match(r"^([A-Za-z0-9_-]+):\s*(.*)$", line)
        if not m:
            errors.append(f"unparsable frontmatter line: {line!r}")
            i += 1
            continue
        key, rest = m.group(1), m.group(2).strip()
        if rest:
            if rest.startswith("[") and rest.endswith("]"):
                items = [x.strip() for x in rest[1:-1].split(",") if x.strip()]
                data[key] = items
            else:
                data[key] = rest
            i += 1
        else:
            items = []
            j = i + 1
            while j < len(fm_lines) and re.match(r"^\s*-\s+\S", fm_lines[j]):
                items.append(re.sub(r"^\s*-\s+", "", fm_lines[j]).strip())
                j += 1
            data[key] = items
            i = j

    return FrontmatterResult(data, body, errors)


def strip_frontmatter_body(text: str) -> str:
    """Return the mini's body with any frontmatter block removed and
    surrounding blank lines normalized to a single trailing newline."""
    return split_frontmatter(text).body.strip("\n") + "\n"


# --------------------------------------------------------------------------
# Skill directory resolution
# --------------------------------------------------------------------------

def resolve_composable_dir(path: Path) -> Path:
    """Accept either a skill root (containing composable/) or the
    composable/ dir itself."""
    if (path / "composable").is_dir():
        return path / "composable"
    return path


def skill_name_for(composable_dir: Path) -> str:
    # composable_dir is .../<name>/composable
    return composable_dir.parent.name


def mini_files(composable_dir: Path) -> list[Path]:
    mdir = composable_dir / "mini"
    if not mdir.is_dir():
        return []
    return sorted(mdir.glob("*.md"), key=lambda p: p.name)


# --------------------------------------------------------------------------
# Versioning
# --------------------------------------------------------------------------

def is_semver(value: str) -> bool:
    return bool(SEMVER_RE.match(value.strip()))


def version_path(composable_dir: Path) -> Path:
    return composable_dir / "VERSION"


def read_skill_version(composable_dir: Path) -> str | None:
    """Return the raw (unvalidated) skill-level VERSION contents, stripped,
    or None if the file doesn't exist."""
    vpath = version_path(composable_dir)
    if not vpath.is_file():
        return None
    text = vpath.read_text(encoding="utf-8").strip()
    return text or None


def bump_semver(version: str, part: str) -> str:
    major, minor, patch = (int(x) for x in version.split("."))
    if part == "major":
        major, minor, patch = major + 1, 0, 0
    elif part == "minor":
        minor, patch = minor + 1, 0
    elif part == "patch":
        patch += 1
    else:
        raise ValueError(f"unknown version part: {part!r}")
    return f"{major}.{minor}.{patch}"


