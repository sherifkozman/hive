def build_bundle_text(
    skill_name: str,
    minis: list[Path],
    title_suffix: str = "",
    skill_version: str | None = None,
) -> str:
    version_suffix = f" (v{skill_version})" if skill_version else ""
    header = (
        f"{GENERATED_MARKER}\n"
        f"# {skill_name}: Compiled Skill Bundle{title_suffix}{version_suffix}\n\n"
        f"All composable mini-skills for `{skill_name}` concatenated in index "
        f"order, frontmatter stripped. Regenerate with "
        f"`tools/hive.py compile <skill-dir>`. Do not edit by hand.\n"
    )
    parts = [header]
    for mini in minis:
        body = strip_frontmatter_body(mini.read_text(encoding="utf-8"))
        parts.append(f"\n---\n<!-- module: {mini.name} -->\n\n{body}")
    full = "".join(parts)
    return full.rstrip("\n") + "\n"


def resolve_preset_ids(minis: list[Path], ids: list[str]) -> tuple[list[Path], list[str]]:
    """Map preset id tokens (e.g. '01', '01-review-method', '01-review-method.md')
    to mini file paths, preserving the given order. Returns (paths, unmatched)."""
    by_full = {p.name: p for p in minis}
    by_stem = {p.stem: p for p in minis}
    by_prefix: dict[str, Path] = {}
    for p in minis:
        prefix = p.stem.split("-", 1)[0]
        by_prefix.setdefault(prefix, p)

    resolved: list[Path] = []
    unmatched: list[str] = []
    for token in ids:
        token = token.strip()
        if not token:
            continue
        match = by_full.get(token) or by_stem.get(token) or by_prefix.get(token)
        if match is None:
            unmatched.append(token)
        else:
            resolved.append(match)
    return resolved, unmatched


def load_presets_json(composable_dir: Path) -> dict[str, list[str]]:
    pj = composable_dir / "presets.json"
    if not pj.is_file():
        return {}
    try:
        data = json.loads(pj.read_text(encoding="utf-8"))
    except json.JSONDecodeError as e:
        print(f"[WARN] presets.json is not valid JSON: {e}", file=sys.stderr)
        return {}
    if not isinstance(data, dict):
        print("[WARN] presets.json must be a JSON object of name -> [ids]", file=sys.stderr)
        return {}
    result: dict[str, list[str]] = {}
    for k, v in data.items():
        if not isinstance(v, list):
            print(f"[WARN] presets.json entry {str(k)!r} is not a list of ids; skipping", file=sys.stderr)
            continue
        result[str(k)] = [str(x) for x in v]
    return result


def cmd_compile(args: argparse.Namespace) -> int:
    composable_dir = resolve_composable_dir(Path(args.skill_dir))
    if not composable_dir.is_dir():
        print(f"error: composable dir not found: {composable_dir}", file=sys.stderr)
        return 1
    skill_name = skill_name_for(composable_dir)
    minis = mini_files(composable_dir)
    if not minis:
        print(f"error: no mini/*.md files found under {composable_dir}", file=sys.stderr)
        return 1

    skill_version = read_skill_version(composable_dir)

    bundle_text = build_bundle_text(skill_name, minis, skill_version=skill_version)
    bundle_path = composable_dir / "BUNDLE.md"
    bundle_path.write_text(bundle_text, encoding="utf-8")
    print(f"wrote {bundle_path} ({fmt_toks(toks(bundle_text))} tokens, {len(minis)} minis)")

    # Presets: merge CLI-provided with presets.json (CLI wins on name clash).
    preset_specs: dict[str, list[str]] = load_presets_json(composable_dir)
    for spec in args.presets or []:
        if "=" not in spec:
            print(f"error: --presets entry must be name=ids, got {spec!r}", file=sys.stderr)
            return 1
        name, ids_csv = spec.split("=", 1)
        preset_specs[name.strip()] = [x.strip() for x in ids_csv.split(",") if x.strip()]

    if preset_specs:
        presets_dir = composable_dir / "presets"
        presets_dir.mkdir(exist_ok=True)
        for name, ids in preset_specs.items():
            resolved, unmatched = resolve_preset_ids(minis, ids)
            if unmatched:
                print(f"error: preset {name!r} references unknown minis: {unmatched}", file=sys.stderr)
                return 1
            preset_text = build_bundle_text(
                skill_name, resolved, title_suffix=f" (preset: {name})", skill_version=skill_version,
            )
            preset_path = presets_dir / f"{name}.md"
            preset_path.write_text(preset_text, encoding="utf-8")
            print(f"wrote {preset_path} ({fmt_toks(toks(preset_text))} tokens, {len(resolved)} minis)")

    return 0


