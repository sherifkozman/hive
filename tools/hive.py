def find_skill_roots(root: Path) -> list[Path]:
    """Recursively find every skill root under ``root`` (any directory at any
    depth containing a ``composable/`` subdirectory) so a categorized layout
    (e.g. ``skills/authored/code-review/``) and a flat one are both discovered."""
    skill_roots = [p.parent for p in root.rglob("composable") if p.is_dir()]
    return sorted(skill_roots, key=lambda p: p.relative_to(root).as_posix())


def cmd_report(args: argparse.Namespace) -> int:
    root = Path(args.skills_root)
    if not root.is_dir():
        print(f"error: skills root not found: {root}", file=sys.stderr)
        return 1

    rows = []
    any_fail = False
    for child in find_skill_roots(root):
        composable_dir = resolve_composable_dir(child)
        minis = mini_files(composable_dir)
        if not minis and not (composable_dir / "INDEX.md").is_file():
            continue  # not a CCS skill dir

        name = child.relative_to(root).as_posix()
        version = read_skill_version(composable_dir) or "-"
        index_path = composable_dir / "INDEX.md"
        index_tokens = toks(index_path.read_text(encoding="utf-8")) if index_path.is_file() else 0.0

        core_path = composable_dir / "mini" / "00-core.md"
        core_tokens = toks(strip_frontmatter_body(core_path.read_text(encoding="utf-8"))) if core_path.is_file() else None

        bundle_path = composable_dir / "BUNDLE.md"
        bundle_tokens = toks(bundle_path.read_text(encoding="utf-8")) if bundle_path.is_file() else None

        mini_token_counts = [toks(strip_frontmatter_body(p.read_text(encoding="utf-8"))) for p in minis]

        r = Reporter()
        lint_skill(composable_dir, r)
        if r.has_fail():
            any_fail = True

        rows.append({
            "name": name,
            "version": version,
            "minis": len(minis),
            "index_tokens": index_tokens,
            "core_tokens": core_tokens,
            "bundle_tokens": bundle_tokens,
            "min_mini": min(mini_token_counts) if mini_token_counts else None,
            "max_mini": max(mini_token_counts) if mini_token_counts else None,
            "lint": f"{r.counts['PASS']}P/{r.counts['WARN']}W/{r.counts['FAIL']}F",
        })

    if not rows:
        print(f"no CCS skill directories found under {root}")
        return 0

    headers = ["skill", "version", "minis", "index_tok", "core_tok", "bundle_tok", "min_mini_tok", "max_mini_tok", "lint (P/W/F)"]

    def fmt(v):
        if v is None:
            return "-"
        if isinstance(v, float):
            return fmt_toks(v)
        return str(v)

    table = [headers]
    for row in rows:
        table.append([
            row["name"], row["version"], str(row["minis"]), fmt(row["index_tokens"]), fmt(row["core_tokens"]),
            fmt(row["bundle_tokens"]), fmt(row["min_mini"]), fmt(row["max_mini"]), row["lint"],
        ])

    widths = [max(len(r[i]) for r in table) for i in range(len(headers))]
    for i, row in enumerate(table):
        line = "  ".join(cell.ljust(widths[j]) for j, cell in enumerate(row))
        print(line)
        if i == 0:
            print("  ".join("-" * w for w in widths))

    return 1 if any_fail else 0


