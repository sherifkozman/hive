def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        prog="hive.py",
        description="Hive skill framework CLI (CCS spec): compile, lint, parity, report, bump.",
    )
    sub = parser.add_subparsers(dest="command", required=True)

    p_compile = sub.add_parser("compile", help="Regenerate BUNDLE.md (and optional presets) from mini/*.md")
    p_compile.add_argument("skill_dir", help="Path to the skill root or its composable/ dir")
    p_compile.add_argument(
        "--presets", nargs="+", metavar="NAME=IDS",
        help="e.g. --presets quick=01,04,05 deep=01,02,03,04,05,06",
    )
    p_compile.set_defaults(func=cmd_compile)

    p_lint = sub.add_parser("lint", help="Check a skill's composable directory for structural issues")
    p_lint.add_argument("skill_dir", help="Path to the skill root or its composable/ dir")
    p_lint.set_defaults(func=cmd_lint)

    p_parity = sub.add_parser("parity", help="Compare a compiled skill's minis against its source material")
    p_parity.add_argument("skill_dir", help="Path to the skill root or its composable/ dir")
    p_parity.add_argument("source_dir", help="Path to the original (pre-conversion) markdown source")
    p_parity.set_defaults(func=cmd_parity)

    p_report = sub.add_parser("report", help="Summarize all CCS skills under a skills root")
    p_report.add_argument("skills_root", help="Path to a directory containing one subdir per skill")
    p_report.set_defaults(func=cmd_report)

    p_bump = sub.add_parser("bump", help="Bump (or initialize) a skill's composable/VERSION")
    p_bump.add_argument("skill_dir", help="Path to the skill root or its composable/ dir")
    p_bump.add_argument(
        "part", nargs="?", choices=["major", "minor", "patch"], default="patch",
        help="Version component to bump (default: patch)",
    )
    p_bump.set_defaults(func=cmd_bump)

    return parser


def main(argv: list[str] | None = None) -> int:
    parser = build_parser()
    args = parser.parse_args(argv)
    return args.func(args)


if __name__ == "__main__":
    raise SystemExit(main())
