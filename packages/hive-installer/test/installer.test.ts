interface FixtureSkillOpts {
  category: string;
  name: string;
  version?: string;
  description?: string;
  coreContent?: string;
  /** Maps asset dir name (e.g. "scripts") -> { relFilePath: content }, mirroring a vendored source's non-.md dirs. */
  assetDirs?: Record<string, Record<string, string>>;
  /** Override the catalog-recorded bundleTokens (default 10 — always well under DEFAULT_INLINE_THRESHOLD). Set above the threshold to force tree under an 'auto' packing option. */
  bundleTokens?: number;
  /** Override BUNDLE.md's body content (default 'bundle content\n'). The generated marker is always prefixed. */
  bundleContent?: string;
  /** Skip writing BUNDLE.md at all (for MissingBundleError coverage). */
  omitBundle?: boolean;
  sourceDescription?: string;
  descriptionSource?: 'upstream' | 'index-fallback';
}

async function writeFixtureSkill(opts: FixtureSkillOpts) {
  const skillDir = path.join(assetsDir, 'skills', opts.category, opts.name);
  const composableDir = path.join(skillDir, 'composable');
  await mkdir(path.join(composableDir, 'mini'), { recursive: true });
  await writeFile(
    path.join(composableDir, 'INDEX.md'),
    `# ${opts.name}\n\n${opts.description ?? 'A test skill.'}\n`,
  );
  await writeFile(path.join(composableDir, 'mini', '00-core.md'), opts.coreContent ?? 'core content');
  if (!opts.omitBundle) {
    await writeFile(
      path.join(composableDir, 'BUNDLE.md'),
      `<!-- GENERATED by tools/hive.py compile. Do not hand-edit. -->\n${opts.bundleContent ?? 'bundle content\n'}`,
    );
  }
  await writeFile(path.join(composableDir, 'VERSION'), opts.version ?? '1.0.0');

  const assetDirNames = Object.keys(opts.assetDirs ?? {}).sort();
  for (const dirName of assetDirNames) {
    const files = opts.assetDirs![dirName]!;
    const assetSrcDir = path.join(skillDir, 'assets-src', dirName);
    await mkdir(assetSrcDir, { recursive: true });
    for (const [relFile, content] of Object.entries(files)) {
      await mkdir(path.dirname(path.join(assetSrcDir, relFile)), { recursive: true });
      await writeFile(path.join(assetSrcDir, relFile), content);
    }
  }

  const description = opts.description ?? 'A test skill.';
  return {
    name: opts.name,
    category: opts.category,
    version: opts.version ?? '1.0.0',
    minis: 1,
    bundleTokens: opts.bundleTokens ?? 10,
    description,
    sourceDescription: opts.sourceDescription ?? description,
    descriptionSource: opts.descriptionSource ?? ('index-fallback' as const),
    path: `skills/${opts.category}/${opts.name}`,
    ...(assetDirNames.length > 0 ? { assetDirs: assetDirNames } : {}),
  };
}

async function makeCatalog(skillOpts: FixtureSkillOpts[]): Promise<Catalog> {
  const skills = [];
  for (const opts of skillOpts) skills.push(await writeFixtureSkill(opts));
  const manifest = {
    generatedAt: new Date(0).toISOString(),
    hiveCommit: 'test-commit',
    skills,
    files: [],
  };
  await writeFile(path.join(assetsDir, 'manifest.json'), JSON.stringify(manifest));
  return loadCatalog(assetsDir);
}

