describe('planInstall', () => {
  it('throws UnknownClientError for an unregistered client id', async () => {
    const catalog = await makeCatalog([{ category: 'authored', name: 'foo' }]);
    await expect(
      planInstall(ctx(), { clients: ['not-a-real-client'], skills: ['foo'], catalog }),
    ).rejects.toThrow(UnknownClientError);
  });

  it('throws UnknownSkillError for a skill not in the catalog', async () => {
    const catalog = await makeCatalog([{ category: 'authored', name: 'foo' }]);
    await expect(
      planInstall(ctx(), { clients: ['claude-code'], skills: ['does-not-exist'], catalog }),
    ).rejects.toThrow(UnknownSkillError);
  });

  it('throws UnsupportedClientError listing every scan-only client requested', async () => {
    const catalog = await makeCatalog([{ category: 'authored', name: 'foo' }]);
    await expect(
      planInstall(ctx(), { clients: ['roo', 'continue', 'claude-code'], skills: ['foo'], catalog }),
    ).rejects.toThrow(UnsupportedClientError);
    try {
      await planInstall(ctx(), { clients: ['roo', 'continue'], skills: ['foo'], catalog });
      throw new Error('expected to throw');
    } catch (err) {
      expect(err).toBeInstanceOf(UnsupportedClientError);
      expect((err as UnsupportedClientError).clientIds.sort()).toEqual(['continue', 'roo']);
    }
  });

  it('plans copy-tree + write-skill-shim + write-install-manifest for a native-skills client, no pointer action', async () => {
    const catalog = await makeCatalog([{ category: 'authored', name: 'foo' }]);
    const plan = await planInstall(ctx(), { clients: ['claude-code'], skills: ['foo'], catalog });

    const kinds = plan.actions.map((a) => a.kind);
    expect(kinds).toEqual(['copy-tree', 'write-skill-shim', 'write-install-manifest']);
    const treeAction = plan.actions[0]!;
    expect(treeAction.kind).toBe('copy-tree');
    if (treeAction.kind === 'copy-tree' || treeAction.kind === 'upgrade') {
      expect(treeAction.destSkillDir).toBe(path.join(homeDir, '.claude', 'skills', 'hive-foo'));
    }
  });

  it('plans a payload tree + exactly ONE pointer-block action per client for a payload-pointer client (gemini), covering multiple skills', async () => {
    const catalog = await makeCatalog([
      { category: 'authored', name: 'foo' },
      { category: 'authored', name: 'bar' },
    ]);
    const plan = await planInstall(ctx(), { clients: ['gemini'], skills: ['foo', 'bar'], catalog });

    const pointerActions = plan.actions.filter((a) => a.kind === 'write-pointer-block');
    expect(pointerActions.length).toBe(1);
    expect(pointerActions[0]).toMatchObject({
      clientId: 'gemini',
      destPath: path.join(homeDir, '.gemini', 'GEMINI.md'),
    });

    const treeActions = plan.actions.filter((a) => a.kind === 'copy-tree' || a.kind === 'upgrade');
    expect(treeActions.length).toBe(2);
    for (const action of treeActions) {
      if (action.kind === 'copy-tree' || action.kind === 'upgrade') {
        expect(action.destSkillDir.startsWith(path.join(homeDir, '.gemini', 'hive-skills'))).toBe(true);
      }
    }
  });

  it('cursor (payload-project-pointer) plans a project pointer only when projectDir is given', async () => {
    const catalog = await makeCatalog([{ category: 'authored', name: 'foo' }]);

    const withoutProject = await planInstall(ctx(), { clients: ['cursor'], skills: ['foo'], catalog });
    expect(withoutProject.actions.some((a) => a.kind === 'write-pointer-block')).toBe(false);
    expect(withoutProject.actions.some((a) => a.kind === 'copy-tree')).toBe(true);

    const projectDir = path.join(tmp, 'my-project');
    const withProject = await planInstall(ctx(), {
      clients: ['cursor'],
      skills: ['foo'],
      catalog,
      projectDir,
    });
    const pointerAction = withProject.actions.find((a) => a.kind === 'write-pointer-block');
    expect(pointerAction).toBeDefined();
    expect(pointerAction).toMatchObject({
      destPath: path.join(projectDir, '.cursor', 'rules', 'hive-skills.mdc'),
    });
  });
});

