describe('executeInstall: idempotency', () => {
  it('a second identical install run reports all skip-identical and does not re-prompt for an unchanged pointer', async () => {
    const catalog = await makeCatalog([{ category: 'authored', name: 'foo' }]);
    let confirmCalls = 0;
    const countingPorts: ExecutePorts = {
      confirmPointerWrite: async () => {
        confirmCalls++;
        return true;
      },
    };

    const plan1 = await planInstall(ctx(), { clients: ['gemini'], skills: ['foo'], catalog });
    await executeInstall(ctx(), plan1, countingPorts, {});
    expect(confirmCalls).toBe(1);

    const plan2 = await planInstall(ctx(), { clients: ['gemini'], skills: ['foo'], catalog });
    expect(plan2.actions.filter((a) => a.kind === 'skip-identical').length).toBe(1);
    expect(plan2.actions.some((a) => a.kind === 'copy-tree' || a.kind === 'upgrade')).toBe(false);

    const result2 = await executeInstall(ctx(), plan2, countingPorts, {});
    expect(confirmCalls).toBe(1); // unchanged pointer content -> no re-prompt
    expect(result2.skipped.some((a) => a.kind === 'skip-identical')).toBe(true);
    expect(result2.performed).toEqual([]);
  });

  it('upgrades when the bundled asset changes: new content installed, manifest updated', async () => {
    const catalog1 = await makeCatalog([{ category: 'authored', name: 'foo', version: '1.0.0', coreContent: 'v1' }]);
    const plan1 = await planInstall(ctx(), { clients: ['claude-code'], skills: ['foo'], catalog: catalog1 });
    await executeInstall(ctx(), plan1, alwaysConfirm, {});

    // Mutate the bundled asset + regenerate the catalog to reflect a new version.
    const catalog2 = await makeCatalog([{ category: 'authored', name: 'foo', version: '1.1.0', coreContent: 'v2' }]);
    const plan2 = await planInstall(ctx(), { clients: ['claude-code'], skills: ['foo'], catalog: catalog2 });

    expect(plan2.actions.map((a) => a.kind)).toEqual(['upgrade', 'write-skill-shim', 'write-install-manifest']);

    const result2 = await executeInstall(ctx(), plan2, alwaysConfirm, {});
    expect(result2.performed.length).toBe(3);

    const destDir = path.join(homeDir, '.claude', 'skills', 'hive-foo');
    const coreContent = await readFile(path.join(destDir, 'composable', 'mini', '00-core.md'), 'utf8');
    expect(coreContent).toBe('v2');
    const manifest = await readInstallManifest(destDir);
    expect(manifest?.skillVersion).toBe('1.1.0');
  });
});

describe('executeInstall: uninstall via restore', () => {
  it('restoring the preinstall backup removes a freshly-installed skill tree and leaves the freshly-created pointer block untouched', async () => {
    const catalog = await makeCatalog([{ category: 'authored', name: 'foo' }]);
    const plan = await planInstall(ctx(), { clients: ['gemini'], skills: ['foo'], catalog });
    const result = await executeInstall(ctx(), plan, alwaysConfirm, {});

    expect(result.backups.length).toBe(1);
    const backupId = result.backups[0]!.id;

    const destDir = path.join(homeDir, '.gemini', 'hive-skills', 'hive-foo');
    const pointerFile = path.join(homeDir, '.gemini', 'GEMINI.md');
    await expect(stat(destDir)).resolves.toBeTruthy();
    const pointerBefore = await readFile(pointerFile, 'utf8');
    expect(pointerBefore).toContain(MANAGED_BLOCK_START);

    await restore(ctx(), backupId, {});

    // Skill tree gone (uninstalled).
    await expect(stat(destDir)).rejects.toThrow();
    // Pointer file untouched: the managed block this install created is
    // NOT auto-removed by restore(), because a not-yet-existing pointer
    // file is deliberately excluded from the preinstall backup (see
    // installer.ts's comment) — restore() never learns about this path
    // and so cannot revert/delete it. This is a documented limitation:
    // a pointer file that pre-existed before install WOULD be captured
    // by the backup and would be fully reverted (block included) by this
    // same restore() call, since backup.ts's restore() has no concept of
    // a managed block (see its own TODO comment).
    const pointerAfter = await readFile(pointerFile, 'utf8');
    expect(pointerAfter).toBe(pointerBefore);
    expect(pointerAfter).toContain(MANAGED_BLOCK_START);
  });

  it('documents the limitation: a pre-existing pointer file IS captured and fully reverted by restore (block removed)', async () => {
    const catalog = await makeCatalog([{ category: 'authored', name: 'foo' }]);
    await mkdir(path.join(homeDir, '.gemini'), { recursive: true });
    await writeFile(path.join(homeDir, '.gemini', 'GEMINI.md'), 'pre-existing rules only\n');

    const plan = await planInstall(ctx(), { clients: ['gemini'], skills: ['foo'], catalog });
    const result = await executeInstall(ctx(), plan, alwaysConfirm, {});
    const backupId = result.backups[0]!.id;

    const pointerFile = path.join(homeDir, '.gemini', 'GEMINI.md');
    expect(await readFile(pointerFile, 'utf8')).toContain(MANAGED_BLOCK_START);

    await restore(ctx(), backupId, {});

    // Known limitation (see backup.ts's restore() TODO): restore is
    // block-unaware, so a pre-existing pointer file is fully reverted,
    // removing the block along with anything else that changed.
    expect(await readFile(pointerFile, 'utf8')).toBe('pre-existing rules only\n');
  });
});

describe('executeInstall: pre-flight foreign-directory refusal', () => {
  it('refuses to overwrite a non-Hive directory occupying the install path without force', async () => {
    const catalog = await makeCatalog([{ category: 'authored', name: 'foo' }]);
    const destDir = path.join(homeDir, '.claude', 'skills', 'hive-foo');
    await mkdir(destDir, { recursive: true });
    await writeFile(path.join(destDir, 'unrelated.txt'), 'not ours');

    const plan = await planInstall(ctx(), { clients: ['claude-code'], skills: ['foo'], catalog });
    await expect(executeInstall(ctx(), plan, alwaysConfirm, {})).rejects.toThrow(/force/);

    // Untouched.
    expect(await readFile(path.join(destDir, 'unrelated.txt'), 'utf8')).toBe('not ours');

    const result = await executeInstall(ctx(), plan, alwaysConfirm, { force: true });
    expect(result.performed.length).toBeGreaterThan(0);
  });
});

