describe('planInstall: packing backward compatibility', () => {
  it('omitting `packing` entirely installs tree mode even for a tiny skill (bundleTokens well under the threshold)', async () => {
    const catalog = await makeCatalog([{ category: 'authored', name: 'foo', bundleTokens: 5 }]);
    const plan = await planInstall(ctx(), { clients: ['claude-code'], skills: ['foo'], catalog });
    expect(plan.actions.map((a) => a.kind)).toEqual(['copy-tree', 'write-skill-shim', 'write-install-manifest']);
  });

  it('packing: "auto" applies the size rule: a tiny skill goes bundle-inline', async () => {
    const catalog = await makeCatalog([{ category: 'authored', name: 'foo', bundleTokens: 5 }]);
    const plan = await planInstall(ctx(), { clients: ['claude-code'], skills: ['foo'], catalog, packing: 'auto' });
    expect(plan.actions.map((a) => a.kind)).toEqual(['write-inline-skill', 'write-install-manifest']);
  });

  it('packing: "auto" applies the size rule: a huge skill stays tree', async () => {
    const catalog = await makeCatalog([
      { category: 'converted', name: 'big', bundleTokens: 195_000, bundleContent: 'x'.repeat(100) },
    ]);
    const plan = await planInstall(ctx(), { clients: ['claude-code'], skills: ['big'], catalog, packing: 'auto' });
    expect(plan.actions.map((a) => a.kind)).toEqual(['copy-tree', 'write-skill-shim', 'write-install-manifest']);
  });

  it('packing: "tree" forces tree even for a tiny skill', async () => {
    const catalog = await makeCatalog([{ category: 'authored', name: 'foo', bundleTokens: 5 }]);
    const plan = await planInstall(ctx(), { clients: ['claude-code'], skills: ['foo'], catalog, packing: 'tree' });
    expect(plan.actions.map((a) => a.kind)).toEqual(['copy-tree', 'write-skill-shim', 'write-install-manifest']);
  });

  it('packing: "bundle-inline" forces inline even for a huge skill', async () => {
    const catalog = await makeCatalog([
      { category: 'converted', name: 'big', bundleTokens: 195_000, bundleContent: 'x'.repeat(100) },
    ]);
    const plan = await planInstall(ctx(), {
      clients: ['claude-code'],
      skills: ['big'],
      catalog,
      packing: 'bundle-inline',
    });
    expect(plan.actions.map((a) => a.kind)).toEqual(['write-inline-skill', 'write-install-manifest']);
  });

  it('--inline-threshold override pulls a mid-size skill into bundle-inline under auto', async () => {
    const catalog = await makeCatalog([{ category: 'authored', name: 'foo', bundleTokens: 26_000 }]);
    const plan = await planInstall(ctx(), {
      clients: ['claude-code'],
      skills: ['foo'],
      catalog,
      packing: 'auto',
      inlineThreshold: 30_000,
    });
    expect(plan.actions.map((a) => a.kind)).toEqual(['write-inline-skill', 'write-install-manifest']);
  });

  it('a forced-inline install with no BUNDLE.md HARD-FAILS with MissingBundleError', async () => {
    const catalog = await makeCatalog([{ category: 'authored', name: 'foo', omitBundle: true }]);
    await expect(
      planInstall(ctx(), { clients: ['claude-code'], skills: ['foo'], catalog, packing: 'bundle-inline' }),
    ).rejects.toThrow(MissingBundleError);
  });
});

describe('renderInlineSkillMd (golden)', () => {
  it('frontmatter carries hive-<skill> + the upstream sourceDescription, YAML-quoted; body is the bundle minus the generated marker', async () => {
    const catalog = await makeCatalog([
      {
        category: 'converted',
        name: 'pdf',
        description: 'INDEX-derived description.',
        sourceDescription: 'Upstream description with a "quote" inside.',
        descriptionSource: 'upstream',
        bundleContent: '# PDF bundle\n\nDo pdf things.\n',
      },
    ]);
    const skill = catalog.skills[0]!;
    const rendered = await renderInlineSkillMd(catalog, skill);

    expect(rendered.startsWith('---\n')).toBe(true);
    expect(rendered).toContain('name: hive-pdf\n');
    expect(rendered).toContain('description: "Upstream description with a \\"quote\\" inside."\n');
    expect(rendered).not.toContain('GENERATED by tools/hive.py compile');
    expect(rendered).not.toContain('INDEX-derived description'); // sourceDescription wins, not description
    expect(rendered.trimEnd().endsWith('# PDF bundle\n\nDo pdf things.'.trimEnd())).toBe(true);
  });

  it('falls back to `description` when a skill has no sourceDescription at all (older/incomplete catalog entry)', async () => {
    const catalog = await makeCatalog([{ category: 'authored', name: 'foo', description: 'Plain description.' }]);
    const skill = catalog.skills[0]!;
    // Simulate an older manifest that never had sourceDescription set.
    const { sourceDescription: _drop, ...rest } = skill;
    void _drop;
    const rendered = await renderInlineSkillMd(catalog, rest as typeof skill);
    expect(rendered).toContain('description: "Plain description."');
  });

  it('rejects with MissingBundleError (typed) when BUNDLE.md is absent', async () => {
    const catalog = await makeCatalog([{ category: 'authored', name: 'foo', omitBundle: true }]);
    const skill = catalog.skills[0]!;
    await expect(renderInlineSkillMd(catalog, skill)).rejects.toThrow(MissingBundleError);
  });
});

describe('executeInstall: bundle-inline packing mode', () => {
  it('installs a SKILL.md with the bundle content, no composable/ dir, and materializes asset dirs', async () => {
    const catalog = await makeCatalog([
      {
        category: 'converted',
        name: 'pdf',
        bundleTokens: 5,
        bundleContent: 'pdf bundle body\n',
        assetDirs: { scripts: { 'check_fillable_fields.py': 'print("check")\n' } },
      },
    ]);
    const plan = await planInstall(ctx(), { clients: ['claude-code'], skills: ['pdf'], catalog, packing: 'auto' });
    const result = await executeInstall(ctx(), plan, alwaysConfirm, {});
    expect(result.performed.length).toBe(2); // write-inline-skill + write-install-manifest, no separate shim

    const destDir = path.join(homeDir, '.claude', 'skills', 'hive-pdf');
    expect(await stat(path.join(destDir, 'composable')).catch(() => undefined)).toBeUndefined();

    const skillMd = await readFile(path.join(destDir, 'SKILL.md'), 'utf8');
    expect(skillMd).toContain('pdf bundle body');
    expect(skillMd).toContain('name: hive-pdf');

    const script = await readFile(path.join(destDir, 'scripts', 'check_fillable_fields.py'), 'utf8');
    expect(script).toBe('print("check")\n');

    const manifest = await readInstallManifest(destDir);
    expect(manifest?.packing).toBe('bundle-inline');
    expect(manifest?.packingForced).toBe(false);
    expect(manifest?.inlineThreshold).toBe(DEFAULT_INLINE_THRESHOLD);
    expect(manifest?.catalogHash).toBe(catalog.hiveCommit);
    expect(manifest?.treeSha256).toBe(await computeInstalledTreeHash(destDir));
  });

  it('a second identical bundle-inline install is skip-identical', async () => {
    const catalog = await makeCatalog([{ category: 'converted', name: 'pdf', bundleTokens: 5 }]);
    const plan1 = await planInstall(ctx(), { clients: ['claude-code'], skills: ['pdf'], catalog, packing: 'auto' });
    await executeInstall(ctx(), plan1, alwaysConfirm, {});

    const plan2 = await planInstall(ctx(), { clients: ['claude-code'], skills: ['pdf'], catalog, packing: 'auto' });
    expect(plan2.actions.map((a) => a.kind)).toEqual(['skip-identical']);
  });

  it('records packingForced: true when --packing explicitly chose the mode', async () => {
    const catalog = await makeCatalog([{ category: 'converted', name: 'pdf', bundleTokens: 5 }]);
    const plan = await planInstall(ctx(), {
      clients: ['claude-code'],
      skills: ['pdf'],
      catalog,
      packing: 'bundle-inline',
    });
    await executeInstall(ctx(), plan, alwaysConfirm, {});
    const destDir = path.join(homeDir, '.claude', 'skills', 'hive-pdf');
    const manifest = await readInstallManifest(destDir);
    expect(manifest?.packingForced).toBe(true);
  });
});

describe('planInstall: upgrade across packing modes (both directions)', () => {
  it('tree -> bundle-inline is a normal upgrade (hash differs), replacing the composable tree with an inline SKILL.md', async () => {
    const catalog = await makeCatalog([{ category: 'authored', name: 'foo', bundleTokens: 5 }]);
    const treePlan = await planInstall(ctx(), { clients: ['claude-code'], skills: ['foo'], catalog, packing: 'tree' });
    await executeInstall(ctx(), treePlan, alwaysConfirm, {});

    const destDir = path.join(homeDir, '.claude', 'skills', 'hive-foo');
    expect(await stat(path.join(destDir, 'composable'))).toBeTruthy();

    const inlinePlan = await planInstall(ctx(), {
      clients: ['claude-code'],
      skills: ['foo'],
      catalog,
      packing: 'auto',
    });
    expect(inlinePlan.actions.map((a) => a.kind)).toEqual(['write-inline-skill', 'write-install-manifest']);
    await executeInstall(ctx(), inlinePlan, alwaysConfirm, {});

    expect(await stat(path.join(destDir, 'composable')).catch(() => undefined)).toBeUndefined();
    expect(await stat(path.join(destDir, 'SKILL.md'))).toBeTruthy();
    const manifest = await readInstallManifest(destDir);
    expect(manifest?.packing).toBe('bundle-inline');
  });

  it('bundle-inline -> tree is a normal upgrade (hash differs), replacing the inline SKILL.md with the composable tree + shim', async () => {
    const catalog = await makeCatalog([{ category: 'authored', name: 'foo', bundleTokens: 5 }]);
    const inlinePlan = await planInstall(ctx(), {
      clients: ['claude-code'],
      skills: ['foo'],
      catalog,
      packing: 'auto',
    });
    await executeInstall(ctx(), inlinePlan, alwaysConfirm, {});

    const destDir = path.join(homeDir, '.claude', 'skills', 'hive-foo');
    expect(await stat(path.join(destDir, 'composable')).catch(() => undefined)).toBeUndefined();

    const treePlan = await planInstall(ctx(), { clients: ['claude-code'], skills: ['foo'], catalog, packing: 'tree' });
    expect(treePlan.actions.map((a) => a.kind)).toEqual(['upgrade', 'write-skill-shim', 'write-install-manifest']);
    await executeInstall(ctx(), treePlan, alwaysConfirm, {});

    expect(await stat(path.join(destDir, 'composable', 'INDEX.md'))).toBeTruthy();
    const manifest = await readInstallManifest(destDir);
    expect(manifest?.packing).toBe('tree');
  });
});

describe('pointer wording change re-prompts consent on upgrade (packing-modes.md v2 item 3)', () => {
  it('an existing managed block with different content triggers confirmPointerWrite again, and is replaced with the current wording', async () => {
    const catalog = await makeCatalog([{ category: 'authored', name: 'foo' }]);
    await mkdir(path.join(homeDir, '.gemini'), { recursive: true });
    const payloadDir = path.join(homeDir, '.gemini', 'hive-skills');
    const oldWordingBlock = [
      '# >>> hive-skills >>>',
      `Hive CCS skills are installed at: ${payloadDir}`,
      "Read a skill's composable/INDEX.md first (knowledge-free loading menu), then apply",
      'the coverage rule from its SKILL.md/SPEC excerpt (<0.6 coverage -> 00-core + selected',
      'minis; >=0.6 -> BUNDLE.md or a matching preset).',
      '# <<< hive-skills <<<',
    ].join('\n');
    await writeFile(path.join(homeDir, '.gemini', 'GEMINI.md'), `my rules\n\n${oldWordingBlock}\n`);

    let confirmCalls = 0;
    let lastDiff = '';
    const countingPorts: ExecutePorts = {
      confirmPointerWrite: async (_file, diff) => {
        confirmCalls++;
        lastDiff = diff;
        return true;
      },
    };

    const plan = await planInstall(ctx(), { clients: ['gemini'], skills: ['foo'], catalog });
    await executeInstall(ctx(), plan, countingPorts, {});

    expect(confirmCalls).toBe(1); // old wording != new wording -> re-prompted, not skipped
    expect(lastDiff).toContain('managed block would change');
    expect(lastDiff).toContain('composable/INDEX.md first (knowledge-free loading menu)'); // old wording shown in the diff

    const updated = await readFile(path.join(homeDir, '.gemini', 'GEMINI.md'), 'utf8');
    expect(updated).toContain("Read a skill's SKILL.md first.");
    expect(updated).not.toContain('composable/INDEX.md first (knowledge-free loading menu)');
  });
});

