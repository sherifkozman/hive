    const projectDir = path.join(tmp, 'my-project');
    const plan = await planInstall(ctx(), { clients: ['cursor'], skills: ['foo'], catalog, projectDir });
    const result = await executeInstall(ctx(), plan, alwaysConfirm, {});

    expect(result.performed.some((a) => a.kind === 'write-pointer-block')).toBe(true);
    const pointerFile = path.join(projectDir, '.cursor', 'rules', 'hive-skills.mdc');
    const content = await readFile(pointerFile, 'utf8');
    expect(content).toContain(MANAGED_BLOCK_START);
